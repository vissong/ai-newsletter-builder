#!/usr/bin/env python3
"""
Scaffold a newsletter site under <dir>.

Usage:
    python init_site.py --dir ./site --design minimal --title "AI Newsletter"

The skill invokes this once during Phase 0. It creates the directory skeleton,
copies the chosen design.md, renders a placeholder style.css from the design
tokens, and writes an empty sources.yaml + site.yaml.

Re-running on an existing site is safe: it only creates missing files.
"""
