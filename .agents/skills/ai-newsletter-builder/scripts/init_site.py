def slurp(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def parse_design(md: str) -> dict:
    """Very small parser for the design.md contract. Extracts:
    - tokens dict (from '## Tokens' bullet list)
    - category_colors dict (from '## Category colors' bullet list)
    - hero snippet (content under '## Hero' up to next heading) or None
    - custom_css (content under '## Custom CSS') or None
    """
    sections: dict[str, str] = {}
    current: str | None = None
    buf: list[str] = []
    for line in md.splitlines():
        m = re.match(r"^(#{1,3})\s+(.*)$", line)
        if m and len(m.group(1)) == 2:
            if current is not None:
                sections[current] = "\n".join(buf).strip()
            current = m.group(2).strip()
            buf = []
            continue
        buf.append(line)
    if current is not None:
        sections[current] = "\n".join(buf).strip()

    def parse_bullets(key: str) -> dict[str, str]:
        body = sections.get(key, "")
        out: dict[str, str] = {}
        for ln in body.splitlines():
            m = re.match(r"^-\s+([A-Za-z0-9_-]+):\s+(.+?)\s*$", ln)
            if m:
                out[m.group(1)] = m.group(2).strip()
        return out

    tokens = parse_bullets("Tokens")
    categories = parse_bullets("Category colors")
    hero = sections.get("Hero") or None
    custom_css = sections.get("Custom CSS") or None
    return {
        "tokens": tokens,
        "categories": categories,
        "hero": hero,
        "custom_css": custom_css,
    }


