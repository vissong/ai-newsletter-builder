def render_css(design: dict) -> str:
    t = design["tokens"]
    c = design["categories"]

    def g(d: dict, k: str, default: str = "") -> str:
        return d.get(k, default)

    replacements = {
        "__BACKGROUND__": g(t, "background", "#fff"),
        "__SURFACE__": g(t, "surface", "#fff"),
        "__INK__": g(t, "ink", "#111"),
        "__INK_MUTED__": g(t, "ink-muted", "#666"),
        "__ACCENT__": g(t, "accent", "#0F62FE"),
        "__ACCENT_SOFT__": g(t, "accent-soft", "#E5EEFF"),
        "__DIVIDER__": g(t, "divider", "#EEE"),
        "__RADIUS__": g(t, "radius", "8"),
        "__MAX_WIDTH__": g(t, "max-width", "760"),
        "__FONT_BODY__": g(t, "font-body", "system-ui, sans-serif"),
        "__FONT_DISPLAY__": g(t, "font-display", "system-ui, sans-serif"),
        "__FONT_MONO__": g(t, "font-mono", "monospace"),
        "__CAT_MAJOR_RELEASE__": g(c, "major-release", "#0F62FE"),
        "__CAT_INDUSTRY_BUSINESS__": g(c, "industry-business", "#047857"),
        "__CAT_RESEARCH_FRONTIER__": g(c, "research-frontier", "#7C3AED"),
        "__CAT_TOOLS_RELEASE__": g(c, "tools-release", "#B45309"),
        "__CAT_POLICY_REGULATION__": g(c, "policy-regulation", "#B91C1C"),
    }
    css = BASE_CSS
    for k, v in replacements.items():
        css = css.replace(k, v)
    if design.get("custom_css"):
        css += "\n/* === custom css from design.md === */\n" + design["custom_css"] + "\n"
    return css


SOURCES_YAML_HEADER = """# Data sources for the newsletter.
# See the skill's references/data_sources.md for field documentation and the
# built-in source catalog.
#
# Each source collects into site/data/raw/<date>/<name>.md.

sources: []
"""


def render_site_yaml(title: str, design: str, output_language: str) -> str:
    return f"""# Site-level configuration.
title: "{title}"
subtitle: "每日 AI 资讯精选"
output_language: {output_language}   # zh | en — used for rendered summaries & lead
timezone: Asia/Shanghai
design: {design}                    # name of built-in design or path to custom design.md
hero_on_homepage: true
"""


def render_readme(title: str) -> str:
    return f"""# {title}

Generated by the `ai-newsletter-builder` skill.

- `index.html` — homepage, regenerated by `scripts/build_index.py`.
- `issues/<date>.html` — one file per daily issue.
- `data/issues.json` — lightweight manifest used by the homepage.
- `config/sources.yaml` — data source definitions.
- `config/design.md` — active design system.

## Rebuild the homepage

```bash
python <path-to-skill>/scripts/build_index.py --site .
```

## Publish

See the skill's `references/publish.md` for GitHub Pages and Cloudflare Pages instructions.
"""


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--dir", required=True, help="Site directory to create/populate")
    ap.add_argument("--design", default="minimal", help="Built-in design name or path to custom design.md")
    ap.add_argument("--title", default="AI Newsletter", help="Site title")
    ap.add_argument("--lang", default="zh", choices=["zh", "en"], help="Output language for summaries")
    args = ap.parse_args()

    site = Path(args.dir).resolve()
    site.mkdir(parents=True, exist_ok=True)

    # Resolve design — two formats:
    #   1) legacy single-file .md (tokens-only) → templated CSS gen
    #   2) directory containing DESIGN.md (Google Stitch / getdesign.md) → placeholder CSS; model must regenerate
    design_arg = args.design
    design_format: str
    design_src: Path

    arg_path = Path(design_arg)
    if arg_path.is_file():
        design_src = arg_path.resolve()
        design_format = "legacy"
    elif arg_path.is_dir() and (arg_path / "DESIGN.md").is_file():
        design_src = arg_path.resolve()
        design_format = "design_md"
    else:
        # Try built-in: directory first, then legacy .md
        builtin_dir = BUILTIN_DESIGNS / design_arg
        builtin_file = BUILTIN_DESIGNS / f"{design_arg}.md"
        if builtin_dir.is_dir() and (builtin_dir / "DESIGN.md").is_file():
            design_src = builtin_dir
            design_format = "design_md"
        elif builtin_file.is_file():
            design_src = builtin_file
            design_format = "legacy"
        else:
            legacy = [p.stem for p in BUILTIN_DESIGNS.glob("*.md") if p.is_file()]
            stitch = [p.name for p in BUILTIN_DESIGNS.iterdir() if p.is_dir() and (p / "DESIGN.md").is_file()]
            print(
                f"error: design '{design_arg}' not found.\n"
                f"  legacy designs (.md):   {', '.join(legacy) or '(none)'}\n"
                f"  DESIGN.md designs (dir): {', '.join(stitch) or '(none)'}",
                file=sys.stderr,
            )
            return 2

    # Create subdirs
    for sub in ("assets", "issues", "data", "data/raw", "config"):
        (site / sub).mkdir(parents=True, exist_ok=True)

    # Copy the chosen design into site/config/
    if design_format == "legacy":
        # Single-file: site/config/design.md + templated style.css
        design_dst = site / "config" / "design.md"
        if not design_dst.exists():
            shutil.copy(design_src, design_dst)
        design = parse_design(slurp(design_dst))
        (site / "assets" / "style.css").write_text(render_css(design), encoding="utf-8")
    else:
        # DESIGN.md directory: site/config/design/<name>/ + placeholder style.css
        dest_dir = site / "config" / "design" / design_src.name
        if not dest_dir.exists():
            shutil.copytree(design_src, dest_dir)
        placeholder = f"""/* Placeholder CSS — overwritten when the skill regenerates from DESIGN.md.
 * Active design: {design_src.name}/DESIGN.md
 * Do NOT edit by hand. Regenerate via the skill.
 */
:root {{
  --color-ink: #111;
  --color-bg: #fff;
  --color-accent: #0F62FE;
  --font-body: system-ui, sans-serif;
  --max-w: 760px;
}}
body {{ margin: 0; background: var(--color-bg); color: var(--color-ink); font-family: var(--font-body); line-height: 1.6; }}
main {{ max-width: var(--max-w); margin: 0 auto; padding: 32px 24px 80px; }}
h1 {{ font-size: 32px; }}
h2 {{ font-size: 22px; margin-top: 40px; }}
h3 {{ font-size: 18px; }}
a {{ color: var(--color-accent); }}
/* TODO: the skill must regenerate this CSS by reading DESIGN.md — until then, the site will look unstyled. */
"""
        (site / "assets" / "style.css").write_text(placeholder, encoding="utf-8")

    # Write site.yaml if missing
    site_yaml = site / "config" / "site.yaml"
    if not site_yaml.exists():
        site_yaml.write_text(
            render_site_yaml(args.title, design_arg if "/" not in design_arg else "custom", args.lang),
            encoding="utf-8",
        )

    # Write sources.yaml if missing
    sources_yaml = site / "config" / "sources.yaml"
    if not sources_yaml.exists():
        sources_yaml.write_text(SOURCES_YAML_HEADER, encoding="utf-8")

    # Initialize issues.json if missing
    issues_json = site / "data" / "issues.json"
    if not issues_json.exists():
        issues_json.write_text(json.dumps({"issues": [], "generated_at": datetime.now(timezone.utc).isoformat()}, indent=2), encoding="utf-8")

    # README
    readme = site / "README.md"
    if not readme.exists():
        readme.write_text(render_readme(args.title), encoding="utf-8")

    print(f"✓ site scaffolded at: {site}")
    print(f"  design: {design_src.name} ({design_format})")
    print(f"  title:  {args.title}")
    print(f"  lang:   {args.lang}")
    if design_format == "design_md":
        print("\n⚠️  DESIGN.md format detected — placeholder CSS was written.")
        print(f"    Next: read {design_src.name}/DESIGN.md and regenerate")
        print(f"    site/assets/style.css (see references/design_md_guide.md).")
    print("\nNext steps:")
    print(f"  1) edit {sources_yaml.relative_to(site.parent)} to add sources")
    print(f"  2) generate today's issue via the skill")
    print(f"  3) run build_index.py to refresh the homepage")
    return 0


if __name__ == "__main__":
    sys.exit(main())
